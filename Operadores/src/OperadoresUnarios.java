public class OperadoresUnarios {
    public static void main(String[] args) {
        int numero = 5;

        numero = - numero;

        System.out.println(- numero);

        System.out.println(numero);

        int numeroNovo = 5;

        System.out.println(--numeroNovo);

        System.out.println(++numeroNovo);

        boolean variavel = true;
        System.out.println(!variavel);

        variavel = !variavel;

        System.out.println(variavel);

    }
}

